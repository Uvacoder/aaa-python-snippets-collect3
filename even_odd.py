print("///Program to check if Entered Number is Even or Odd///")
print("\nEnter a Number: ")
a = input()
if int(a) % 2 == 0:
    print("Entered Number {0} is Even".format(a))
else:
    print("Entered Number {0} is Odd".format(a))
